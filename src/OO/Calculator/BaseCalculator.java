package OO.Calculator;

public class BaseCalculator {

    public double plus(double a, double b){
        return a + b;
    }
    public double minus(double a, double b){
        return a - b;
    }
    public double multiply(double a, double b){
        return a * b;
    }
    public double div(double a, double b){
        return a / b;
    }
}
